                            <h2 class="blog-title">Những món ăn "chỉ có" trong ngày Tết thời xưa</h2>
                            <p>Sủi cảo là món ăn truyền thống nổi tiếng của người Trung Quốc. Với phong vị tuyệt hảo,
                                món
                                ăn này nhanh chóng được đông đảo người dân khắp châu Á yêu thích, trong đó có Việt Nam.
                                Trong bài viết này ChiChiFood sẽ hướng dẫn bạn 3 cách chế biến món sủi cảo nóng hổi siêu
                                ngon tại nhà.
                            </p>
                            <h3 class="h4 mt-40">Hướng dẫn làm vỏ bánh sủi cảo tại nhà</h3>
                            <p>Trước khi đi vào hướng dẫn chi tiết từng món ăn, chúng tôi sẽ hướng dẫn bạn làm
                                vỏ bánh sủi cảo đúng cách nhé.</p>
                            <img src="assets/img/blog/blog-001.jpg"><br><br>
                            <p><strong>Nguyên liệu cần chuẩn bị:</strong>
                            <ul>
                                <li>Bột mì: 250gr</li>
                                <li>Trứng gà: 1 quả</li>
                                <li>Bột bắp: 100gr</li>
                                <li>Muối tinh: 2gr</li>
                                <li>Nước đun sôi để nguội</li>
                            </ul>
                            </p>
                            <p><strong>Cách thực hiện:</strong></p>
                            <ul>
                                <li><i>Bước 1: Cách trộn bột</i></li>
                                <p>Cho bột mì vào bát lớn, pha 130ml nước muối loãng (cho một xíu muối thôi không vỏ
                                    bánh
                                    sẽ bị mặn). Sau đó, đổ nước đó vào bát bột rồi trộn đều. Tiếp đến, bạn đập 1 quả
                                    trứng
                                    vào và đánh đều tay, nếu như quá khô bạn đổ thêm khoảng 7 - 10ml nước.
                                    Nguyên tắc khi trộn bột là bột không được quá khô hoặc nhão.
                                </p>
                                <li><i>Bước 2: Cách nhào bột</i></li>
                                <p>Bạn đeo bao tay nilon trắng, đổ bột ra 1 mặt phẳng sạch và dùng tay nhào thật mạnh
                                    trong 10 phút. Sau khi thấy bột đã dẻo thì dừng lại se bột thành các thanh tròn dài
                                    đường kính khoảng 4 cm.
                                    Để bột nghỉ khoảng 30 phút thì bắt đầu làm tiếp.
                                </p>
                                <li><i>Bước 3: Cách tạo hình vỏ bánh</i></li>
                                <p>Bạn lấy 1 lượng bột vừa đủ nặn thành hình tròn. Sau đó, lấy thanh gỗ tán qua tán lại
                                    cho vỏ bánh dẹp xuống thành hình tròn mỏng. Tránh tán rách vỏ bánh, để độ dày vừa
                                    phải thì bánh sẽ ngon hơn.
                                </p>
                            </ul>
                            <h3 class="h4 mt-40">Hướng dẫn 2 cách làm món sủi cảo ngon chuẩn vị</h3>
                            <h4 class="h6">1. Sủi cảo nhân thịt</h4>
                            <img src="assets/img/blog/blog-002.jpg"><br><br>
                            <p class="mb-0">Đây là loại nhân sủi cảo được nhiều bạn yêu thích nhất. Lớp vỏ sủi cảo mềm
                                dẻo nóng hổi, nhân thịt thơm ngon khó cưỡng bên trong tạo ra điểm nhấn riêng cho món ăn
                                nổi tiếng này. ChiChiFood mời bạn cùng bắt tay vào chế biến món sủi cảo nhân thịt hấp
                                dẫn này ngay thôi.</p>
                            <p><strong>Nguyên liệu cần chuẩn bị:</strong></p>
                            <ul>
                                <li>Thịt nạc : 500g.<br>
                                    Nên chọn loại thịt nạc có pha mỡ một chút như vậy món sủi cảo sẽ ngọt và béo ngậy
                                    hơn. Không nên chọn hết thịt nạc vì món ăn sẽ hơi bị khô.</li>
                                <li>Trứng gà; 2 quả</li>
                                <li>Lá cải thảo: 4 - 5 lá</li>
                                <li>Rau thơm: Hành lá, Gừng, Rau mùi</li>
                                <li>Gia vị: Hạt nêm, Xì dầu, Hạt tiêu ( Dùng loại đã xay sẵn)</li>
                            </ul>
                            <ul>
                                <li><i>Bước 1: Cách sơ chế nguyên liệu</i></li>
                                <p>Đối với phần thịt nạc, bạn thái nhỏ thành từng miếng rồi băm nhuyễn. Để tiết kiệm
                                    thời gian hơn, bạn có thể cho vào máy xay thịt (tuy nhiên đừng xay quá nhuyễn sẽ
                                    khiến nhân thịt không ngon).
                                    Tiếp đến, rửa sạch cải thảo, sau đó thái chỉ và ngâm trong nước muối khoảng 10 phút.
                                    Các loại rau thơm như: Gừng, hành , rau mùi rửa sạch và băm nhỏ.
                                </p>
                                <li><i>Bước 2: Tạo hình sủi cảo</i></li>
                                <p>Đầu tiên bạn cần nêm nếm các loại gia vị vừa miệng vào các nguyên liệu đã sơ chế ở
                                    bước 1. Thêm hạt nêm, xì dầu, hạt tiêu vừa ăn sau đó trộn đều. Tiếp đến, đập 1 quả
                                    trứng vào và trộn đều tay để trứng quyện vào các nguyên liệu khác.
                                    Sau đó, bạn chỉ cần lấy một miếng vỏ bánh, cho 1 chút nhân thịt vào giữa và tạo hình
                                    cho món sủi cảo.
                                </p>
                            </ul>
                            <h4 class="h6">2. Sủi cảo nhân tôm</h4>
                            <img src="assets/img/blog/blog-003.jpg"><br><br>
                            <p class="mb-0">Ngoài sủi cảo nhân thịt thì sủi cảo nhân tôm cũng là hương vị được nhiều
                                người yêu thích, đặc biệt là với các bạn thích ăn hải sản. Ngay sau đây ChiChiFood sẽ
                                hướng dẫn bạn làm món sủi cảo nhân tôm thơm ngon ngay tại nhà nhé.</p>
                            <p><strong>Nguyên liệu cần chuẩn bị:</strong></p>
                            <ul>
                                <li>Tôm sú tươi: 200gr - 300gr</li>
                                <li>Cải thảo: ½ cây</li>
                                <li>Cà rốt: ½ củ</li>
                                <li>Nấm hương: Cho nhiều hoặc ít tùy sở thích</li>
                                <li>Gia vị: Hạt Nêm, Hạt tiêu, Dầu mè</li>
                            </ul>
                            <p><strong>Cách chế biến:</strong></p>
                            <ul>
                                <li><i>Bước 1: Cách sơ chế nguyên liệu</i></li>
                                <p>Đầu tiên, bạn cần rửa sạch tôm và ngâm tôm trong nước muối khoảng 10 phút để tôm bớt
                                    tanh. Sau đó, bạn vớt ra để ráo nước qua rồi băm nhỏ tôm<br>
                                    Đối với cải thảo thái sợi nhỏ. Cà rốt băm nhuyễn.<br>
                                    Nấm hương bạn ngâm trong nước ấm, khi thấy nấm hương đã nở ra và mềm hơn thì rửa
                                    sạch và thắt thành các miếng nhỏ.
                                </p>
                                <li><i>Bước 2: Tạo hình sủi cảo</i></li>
                                <p>Bạn trộn tất cả các nguyên liệu đã sơ chế ở bước 1, nêm nếm gia vị vừa miệng rồi trộn
                                    đều. Sau đó, lấy 1 quả trứng đập vào hỗn hợp trên rồi đánh đều tay sao cho trứng
                                    quyện vào các nguyên liệu khác.
                                    Tiếp đến, bạn lấy một miếng vỏ bánh và một ít nhân tôm, gói lại, tạo hình sủi cảo là
                                    hoàn thành.
                                </p>
                            </ul>
